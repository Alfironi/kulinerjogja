package com.kulinerjogja.model;

public class MakananModel {

	String idMakanan;
	String namaMakanan;
	String idKategori;
	String idRestoran;
	String namaRestoran;
	String namaKategori;
	public String getIdMakanan() {
		return idMakanan;
	}
	public void setIdMakanan(String idMakanan) {
		this.idMakanan = idMakanan;
	}
	public String getNamaMakanan() {
		return namaMakanan;
	}
	public void setNamaMakanan(String namaMakanan) {
		this.namaMakanan = namaMakanan;
	}
	public String getIdKategori() {
		return idKategori;
	}
	public void setIdKategori(String idKategori) {
		this.idKategori = idKategori;
	}
	public String getIdRestoran() {
		return idRestoran;
	}
	public void setIdRestoran(String idRestoran) {
		this.idRestoran = idRestoran;
	}
	public String getNamaRestoran() {
		return namaRestoran;
	}
	public void setNamaRestoran(String namaRestoran) {
		this.namaRestoran = namaRestoran;
	}
	public String getNamaKategori() {
		return namaKategori;
	}
	public void setNamaKategori(String namaKategori) {
		this.namaKategori = namaKategori;
	}
	
}
